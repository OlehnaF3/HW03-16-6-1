namespace HW03_16_6_1
{
    public class Calculator
    {
        /// <summary>
        /// Метод сложения чисел
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>

        public int Additional(int a, int b)
        {
            return a + b;
        }
        /// <summary>
        /// Метод вычитания чисел
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>

        public int Subtraction(int a, int b)
        {
            return a - b;
        }
        /// <summary>
        /// Метод умножения
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>

        public int Miltiplication(int a, int b)
        {
            return a * b;
        }
        /// <summary>
        /// Метод деления
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>

        public int Division(int a, int b)
        {
            return a / b;
        }
    }
}

